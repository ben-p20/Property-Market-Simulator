import kagglehub

# Download latest version
path = kagglehub.dataset_download("shashanknecrothapa/ames-housing-dataset")

print("Path to dataset files:", path)

import os
import pandas as pd

dataset_dir = "/kaggle/input/ames-housing-dataset"

print("Files in directory:", os.listdir(dataset_dir))

csv_path = os.path.join(dataset_dir, 'AmesHousing.csv')

# Load it into a Pandas DataFrame
df = pd.read_csv(csv_path)

# Look at the first 5 rows
df.head()

print(df.columns.tolist())

"""**Filtering dataframe to necessary columns**"""

selected_columns = ['SalePrice', 'Neighborhood', 'Year Built', 'TotRms AbvGrd', 'Bldg Type', 'Garage Cars', 'Paved Drive']

project_df = df[selected_columns].copy()

project_df['Garage Cars'] = project_df['Garage Cars'].fillna(0)

project_df['House Age'] = 2010 - project_df['Year Built']
project_df.drop(columns=['Year Built'], inplace=True)

print(project_df.head())
print("\nUnique Building types:", project_df['Bldg Type'].unique())
print("Unique Driveways:", project_df['Paved Drive'].unique())
print("Unique Neighborhood types:", project_df['Neighborhood'].unique())

"""**Combine Neighborhoods into tiered locations (Budget, Average, Premium)**"""

neigh_prices = project_df.groupby('Neighborhood')['SalePrice'].median().sort_values()
print("Neighborhood Mean Prices")
print(neigh_prices)

def assign_tier(price):
  if price < 140000:
     return 'Budget Tier'
  elif price < 220000:
     return 'Average Tier'
  else:
     return 'Premium Tier'

neigh_tier_map = neigh_prices.apply(assign_tier).to_dict()

project_df['Location Tier'] = project_df['Neighborhood'].map(neigh_tier_map)
project_df.drop(columns=['Neighborhood'], inplace=True)


print(project_df.head())
print(project_df['Location Tier'].value_counts())

"""**Preparing for linear regression model**"""

project_df['Paved Drive'] = project_df['Paved Drive'].map({'Y': 1, 'N': 0, 'P': 0})

encoded_df = pd.get_dummies(project_df, columns=['Bldg Type', 'Location Tier'], drop_first=True, dtype=int)

print(encoded_df.head())

"""**Linear regression model to find baseline price and coefficients**"""

from sklearn.linear_model import LinearRegression

X = encoded_df.drop(columns=['SalePrice']) # Independent Variables: The features used for the prediction, everything except sale price
y = encoded_df['SalePrice'] # Dependent Variables: The prediction target (Sale Price)

model = LinearRegression()
model.fit(X, y)

print(f"Baseline Price (Intercept): ${model.intercept_:,.2f}") # Model.intercept represents the starting cost if all features in X were 0.
print("\nAttribute Weights (Coefficients):")

for feature, coef in zip(X.columns, model.coef_): # Loops through  every iteration of the zip (which takes two lists of equal length and locks them together), naming each column text as the feature with its weight from coef
    print(f"-> {feature:<30} : ${coef:,.2f}")  #:<30 is used to align the text to the left padded out to 30 characters so each : and $ sign line up

"""**Standard Error & Standard Deviation**"""

import numpy as np

real_std = np.std(y)

predictions = model.predict(X)
residuals = y - predictions
residual_std_error = np.std(residuals)

print(f"Real market standard deviation (total spread): ${real_std:,.2f}")
print(f"Model residual standard error (noise scale): ${residual_std_error:,.2f}")